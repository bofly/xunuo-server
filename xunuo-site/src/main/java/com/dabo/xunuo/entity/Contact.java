package com.dabo.xunuo.entity;

public class Contact {
	private int id;
	private String name;
	private String birthday;
	private String phone;
	private String remark;
	private int figure_id;
	private int contact_type_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public int getFigure_id() {
		return figure_id;
	}
	public void setFigure_id(int figure_id) {
		this.figure_id = figure_id;
	}
	public int getContact_type_id() {
		return contact_type_id;
	}
	public void setContact_type_id(int contact_type_id) {
		this.contact_type_id = contact_type_id;
	}
	
	
	

}
